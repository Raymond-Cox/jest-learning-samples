/**
 * Adds " World" to end of provided string.
 * @param {String} str
 * @returns {String}
 */
export function addWorldToString(str) {
    return str + ' World'
}

/**
 * Adds two numbers together, returns the sum.
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export function add(a, b) {
    return a + b
}

/**
 * Subtracts b from a, returns the difference.
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export function subtract(a, b) {
    return a - b
}

/**
 * Multiplies a by b, returns the product.
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export function multiply(a, b) {
    return a * b
}

/**
 * Divides a by b, returns the quotient.
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export function divide(a, b) {
    return a / b
}
